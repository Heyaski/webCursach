document.addEventListener('DOMContentLoaded', function () {
	let selectedCourseId = null

	// Открытие модального окна и сохранение выбранного курса
	document.querySelectorAll('.enroll-btn').forEach(btn => {
		btn.addEventListener('click', function () {
			selectedCourseId = this.dataset.id // Сохраняем ID выбранного курса
			document.body.style.overflow = 'hidden'
		})
	})

	// Закрытие модального окна
	document
		.querySelector('[href="#close"]')
		.addEventListener('click', function () {
			document.body.style.overflow = 'visible'
		})

	// Обработка отправки формы
	document
		.querySelector('.modal form')
		.addEventListener('submit', function (event) {
			event.preventDefault() // Предотвращаем стандартное поведение формы

			// Меняем текст ссылки для выбранного курса
			// if (selectedCourseId) {
			// 	const courseLink = document.querySelector(
			// 		`.enroll-btn[data-id="${selectedCourseId}"]`
			// 	)
			// 	courseLink.textContent = 'Ожидайте подтверждения'
			// 	courseLink.classList.add('disabled') // Добавляем класс для стилей
			// 	courseLink.removeAttribute('href') // Убираем ссылку
			// }

			// Закрываем модальное окно
			document.querySelector('[href="#close"]').click()
		})

	// Обработчик для кнопок записи
	document.querySelectorAll('.enroll-btn').forEach(button => {
		button.addEventListener('click', function (e) {
			document.getElementById('courseId').value = this.dataset.id
		})
	})

	document.getElementById('aboutMe').addEventListener('input', function () {
		const value = this.value
		const errorText = document.getElementById('errorText')

		// Список запрещённых слов
		const bannedWords = ['мат', 'плохое', 'ругательство', 'брань']

		// Заменяем запрещённые слова на '...'
		let filteredText = value
		bannedWords.forEach(word => {
			const regex = new RegExp(word, 'gi')
			filteredText = filteredText.replace(regex, '...')
		})

		// Обновляем значение текстового поля
		if (this.value !== filteredText) {
			this.value = filteredText
		}

		// Проверка осмысленности текста (не пустой, не только цифры, не только спецсимволы)
		const isMeaningful =
			/[A-Za-zА-Яа-я]/.test(filteredText) && !/^\d+$/.test(filteredText)

		if (isMeaningful) {
			this.classList.remove('is-invalid')
			errorText.classList.add('d-none')
		} else {
			this.classList.add('is-invalid')
			errorText.classList.remove('d-none')
		}
	})

	// Обработка отправки формы
	document
		.getElementById('enrollmentForm')
		.addEventListener('submit', function (e) {
			e.preventDefault()

			const formData = new FormData(this)

			fetch('/enroll-course/', {
				method: 'POST',
				body: formData,
				headers: {
					'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')
						.value,
				},
			})
				.then(response => response.json())
				.then(data => {
					if (data.status === 'success') {
						alert(data.message)
						window.location.reload()
					} else {
						alert(data.message)
					}
				})
				.catch(error => {
					console.error('Error:', error)
					alert('Произошла ошибка при отправке формы')
				})
		})

	console.log('Script loaded') // Отладочная информация

	// Добавляем обработчик для поля телефона
	const phoneInput = document.getElementById('phone')
	if (phoneInput) {
		console.log('Phone input found') // Отладочная информация

		phoneInput.addEventListener('input', event => {
			let input = phoneInput.value.replace(/\D/g, '') // Удалить все нецифровые символы
			let formatted = '+7'

			if (input.length > 1) {
				formatted += ' (' + input.substring(1, 4)
				if (input.length >= 4) {
					formatted += ') ' + input.substring(4, 7)
					if (input.length >= 7) {
						formatted += '-' + input.substring(7, 9)
						if (input.length >= 9) {
							formatted += '-' + input.substring(9, 11)
						}
					}
				}
			}

			phoneInput.value = formatted
		})

		// Если телефон уже есть в поле, применяем маску
		if (phoneInput.value) {
			const event = new Event('input')
			phoneInput.dispatchEvent(event)
		}
	}

	// Обработка формы записи
	const enrollmentForm = document.getElementById('enrollmentForm')
	if (enrollmentForm) {
		console.log('Form found') // Отладочная информация

		enrollmentForm.addEventListener('submit', function (e) {
			e.preventDefault()
			console.log('Form submitted') // Отладочная информация

			const formData = new FormData(this)
			console.log('Form data:', Object.fromEntries(formData)) // Отладочная информация

			fetch('/enroll-course/', {
				method: 'POST',
				body: formData,
				headers: {
					'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')
						.value,
				},
				credentials: 'same-origin',
			})
				.then(response => response.json())
				.then(data => {
					console.log('Response data:', data) // Отладочная информация
					if (data.status === 'success') {
						// Находим кнопку для текущего курса
						const courseId = formData.get('course_id')
						const courseButton = document.querySelector(
							`.enroll-btn[data-id="${courseId}"]`
						)
						if (courseButton) {
							// Заменяем кнопку на статус "Ожидает подтверждения"
							const enrollmentContainer = courseButton.closest(
								'.enrollment-container'
							)
							if (enrollmentContainer) {
								enrollmentContainer.innerHTML =
									'<div class="enrollment-status pending">Ожидает подтверждения</div>'
							}
						}

						// Закрываем модальное окно и показываем сообщение
						window.location.href = '#close'
						alert('Заявка на обучение успешно отправлена')
					}
				})
				.catch(error => {
					console.error('Error:', error)
				})
		})
	}

	// Обработчики для кнопок записи
	document.querySelectorAll('.enroll-btn').forEach(button => {
		button.addEventListener('click', function () {
			console.log('Button clicked, course ID:', this.dataset.id) // Отладочная информация
			const courseIdInput = document.getElementById('courseId')
			if (courseIdInput) {
				courseIdInput.value = this.dataset.id
			}
		})
	})

	// Обработчик для поля города в модальном окне записи на курс
	const cityInput = document.getElementById('cityInput')
	const suggestions = document.getElementById('suggestions')

	if (cityInput && suggestions) {
		cityInput.addEventListener('input', function () {
			const query = this.value.toLowerCase()
			suggestions.innerHTML = ''

			if (query.length >= 2) {
				const filteredCities = cities
					.filter(city => city.toLowerCase().includes(query))
					.slice(0, 5) // Показываем только первые 5 совпадений

				filteredCities.forEach(city => {
					const div = document.createElement('div')
					div.textContent = city
					div.addEventListener('click', function () {
						cityInput.value = city
						suggestions.innerHTML = ''
					})
					suggestions.appendChild(div)
				})
			}
		})

		// Добавляем валидацию при отправке формы
		document
			.getElementById('enrollmentForm')
			.addEventListener('submit', function (e) {
				const cityValue = cityInput.value
				if (!cities.includes(cityValue)) {
					e.preventDefault()
					alert('Пожалуйста, выберите город из списка')
					cityInput.focus()
					return false
				}
			})
	}
})

