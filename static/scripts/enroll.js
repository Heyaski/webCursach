const cityInput = document.getElementById('cityInput')
const suggestions = document.getElementById('suggestions')

// Обработчик ввода
cityInput.addEventListener('input', () => {
	const query = cityInput.value.toLowerCase()
	suggestions.innerHTML = '' // Очистка предыдущих предложений

	if (query) {
		// Фильтрация городов по введенному значению
		const filteredCities = cities.filter(city =>
			city.toLowerCase().includes(query)
		)

		// Отображение предложений
		filteredCities.forEach(city => {
			const div = document.createElement('div')
			div.textContent = city

			// Выбор города при клике
			div.addEventListener('click', () => {
				cityInput.value = city
				suggestions.innerHTML = '' // Очистить предложения после выбора
			})

			suggestions.appendChild(div)
		})
	}
})

// Скрытие списка при клике вне поля ввода
document.addEventListener('click', event => {
	if (!event.target.closest('.input-box')) {
		suggestions.innerHTML = ''
	}
})

document.addEventListener('DOMContentLoaded', function () {
	console.log('Script loaded')

	// Обработка формы записи
	const enrollmentForm = document.getElementById('enrollmentForm')
	if (enrollmentForm) {
		console.log('Form found')

		enrollmentForm.addEventListener('submit', function (e) {
			e.preventDefault()
			console.log('Form submitted')

			const formData = new FormData(this)

			fetch('/enroll-course/', {
				method: 'POST',
				body: formData,
				headers: {
					'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')
						.value,
				},
				credentials: 'same-origin',
			})
				.then(response => response.json())
				.then(data => {
					console.log('Response data:', data)
					if (data.status === 'success') {
						// Находим кнопку для текущего курса
						const courseId = formData.get('course_id')
						const courseButton = document.querySelector(
							`.enroll-btn[data-id="${courseId}"]`
						)
						if (courseButton) {
							// Заменяем кнопку на статус "Ожидает подтверждения"
							const enrollmentContainer = courseButton.closest(
								'.enrollment-container'
							)
							if (enrollmentContainer) {
								enrollmentContainer.innerHTML =
									'<div class="enrollment-status pending">Ожидает подтверждения</div>'
							}
						}

						// Закрываем модальное окно и показываем сообщение
						window.location.href = '#close'
						alert('Заявка на обучение успешно отправлена')
					}
				})
				.catch(error => {
					console.error('Error:', error)
				})
		})
	}

	// Обработчик для кнопок записи
	document.querySelectorAll('.enroll-btn').forEach(button => {
		button.addEventListener('click', function () {
			console.log('Button clicked, course ID:', this.dataset.id)
			document.getElementById('courseId').value = this.dataset.id
		})
	})

	// Обработчик для поля телефона
	const phoneInput = document.getElementById('phone')
	if (phoneInput) {
		phoneInput.addEventListener('input', function (e) {
			let input = e.target.value.replace(/\D/g, '')
			let formatted = '+7'

			if (input.length > 1) {
				formatted += ' (' + input.substring(1, 4)
				if (input.length >= 4) {
					formatted += ') ' + input.substring(4, 7)
					if (input.length >= 7) {
						formatted += '-' + input.substring(7, 9)
						if (input.length >= 9) {
							formatted += '-' + input.substring(9, 11)
						}
					}
				}
			}

			phoneInput.value = formatted
		})

		// Применяем маску к существующему номеру
		if (phoneInput.value) {
			const event = new Event('input')
			phoneInput.dispatchEvent(event)
		}
	}

	// Обработчик для кнопки отказа
	document
		.getElementById('confirmDecline')
		.addEventListener('click', function () {
			const enrollmentId = this.dataset.enrollmentId

			fetch(`/decline-course/${enrollmentId}/`, {
				method: 'POST',
				headers: {
					'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')
						.value,
					'Content-Type': 'application/json',
				},
				credentials: 'same-origin',
			})
				.then(response => response.json())
				.then(data => {
					if (data.status === 'success') {
						window.location.reload()
					} else {
						alert(data.message)
					}
				})
				.catch(error => {
					console.error('Error:', error)
				})

			$('#confirmationModal').modal('hide')
		})

	// Обработчик для открытия модального окна подтверждения
	document
		.querySelectorAll('.btn-danger[data-toggle="modal"]')
		.forEach(button => {
			button.addEventListener('click', function () {
				const enrollmentId =
					this.closest('.list-group-item').dataset.enrollmentId
				document.getElementById('confirmDecline').dataset.enrollmentId =
					enrollmentId
			})
			function declineCourse(enrollmentId) {
				if (!confirm('Вы уверены, что хотите отменить запись на курс?')) {
					return
				}

				fetch(`/decline-course/${enrollmentId}/`, {
					method: 'POST',
					headers: {
						'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')
							.value,
						'Content-Type': 'application/json',
					},
					credentials: 'same-origin',
				})
					.then(response => response.json())
					.then(data => {
						if (data.status === 'success') {
							window.location.reload()
						} else {
							alert(data.message || 'Произошла ошибка при отмене записи')
						}
					})
					.catch(error => {
						console.error('Error:', error)
						alert('Произошла ошибка при отмене записи')
					})
			}
			// Обработчик для поля города в модальном окне записи на курс
			const cityInput = document.getElementById('cityInput')
			const suggestions = document.getElementById('suggestions')

			if (cityInput && suggestions) {
				cityInput.addEventListener('input', function () {
					const query = this.value.toLowerCase()
					suggestions.innerHTML = ''

					if (query.length >= 2) {
						const filteredCities = cities
							.filter(city => city.toLowerCase().includes(query))
							.slice(0, 5) // Показываем только первые 5 совпадений

						filteredCities.forEach(city => {
							const div = document.createElement('div')
							div.textContent = city
							div.addEventListener('click', function () {
								cityInput.value = city
								suggestions.innerHTML = ''
							})
							suggestions.appendChild(div)
						})
					}
				})

				// Добавляем валидацию при отправке формы
				document
					.getElementById('enrollmentForm')
					.addEventListener('submit', function (e) {
						const cityValue = cityInput.value
						if (!cities.includes(cityValue)) {
							e.preventDefault()
							alert('Пожалуйста, выберите город из списка')
							cityInput.focus()
							return false
						}
					})
			}
		})
})

